
// Escape characters in JAVA


class EscapeCharacters {

public static void main(String[] args) {

	System.out.println("Hello \nworld!"); // New line
	System.out.println("Hello \t world!"); // Tab shift
	System.out.println("Sara is in java class \rZ"); // Carriage return
	System.out.println("Hello \\world!"); // For backslash in print statements
	System.out.println("Hello \"student's"); // For adding single/double quotes
}
}